# int(x) - Convert to integer
print(int(3.7))          

# float(x) - Convert to floating point
print(float(5))        

# str(x) - Convert to string
print(str(123))         

# tuple(x) - Convert to tuple
print(tuple([1, 2, 3]))  

# list(x) - Convert to list
print(list((4, 5, 6)))  

# set(x) - Convert to set
print(set([1, 2, 2, 3])) 

# ord(x) - Character to integer (ASCII/Unicode code point)
print(ord('A'))         

# chr(x) - Integer to character
print(chr(65))           

# oct(x) - Integer to octal string
print(oct(10))         

# hex(x) - Integer to hexadecimal string
print(hex(255))          

# dict(x) - Create dictionary from key-value pairs
print(dict([('a', 1), ('b', 2)]))  