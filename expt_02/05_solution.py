# update a new value to set:-
Set1 = {1, 2, 3, 4, 5}
set2 = {6, 7, 8}
Set1.update(set2)
print(Set1)
# pop- removes a random value:-
set1 = {1, 2, 3, 4, 5}
set1.pop()
print(set1)
# remove- removes a specific element:-
set1 = {1, 2, 3, 4, 5}
set1.remove(4)
print(set1)
# add- addition of a new element:-
set1 = {1, 2, 3, 4, 5}
set1.add(6)
print(set1)
# clear- empties the set:-
set1 = {1, 2, 3, 4, 5}
set1.clear()
print(set1)